//Practice declaring and initializing variables of different data types.
#include <stdio.h>
int main()
{
    //Data type: Integer (int).
    //Initialization:
    int a;
    int b, c, d, e;

    //Declaration:
    a=e;
    b=3;
    int c1=4;
    int a1, b1=3, c2, d1;


    //Data type: Float (float).
    //Initialization:
    float f;
    float g, h, i2, j, k;

    //Declaration:
    f=g;
    h=3.3;
    float i1=4;
    float f1, g1=3, h1, i;

    //Data type: Character (char).
    //Initialization:
    char l1;
    char m, m1, n1, o1;

    //Declaration:
    o1=m;
    m='j';
    char n2='k';
    char l, n, o;


    //Data type: String (char* or char string_name[]).
    //Initialization:
    char *p;
    char q1[4];
    char *r1, s1, t1;
    char u1[5], v[9], x[1];

    //Declaration:
    p="JKC";
    char * q="Jampani Komal";
    char r, s[8]="Chowdary", t, u;
    
    //For int, float and char:
    a=b; //Assigning b to a.
    a==b; //Comparing a and b.


    return 0;
}